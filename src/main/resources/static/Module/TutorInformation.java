package com.sample;


public class TutorInformation {

  private long tid;
  private String direction;
  private String tel;
  private String email;
  private long scholarship;
  private String tutorRequire;
  private long display;
  private long enrollmentNumber;


  public long getTid() {
    return tid;
  }

  public void setTid(long tid) {
    this.tid = tid;
  }


  public String getDirection() {
    return direction;
  }

  public void setDirection(String direction) {
    this.direction = direction;
  }


  public String getTel() {
    return tel;
  }

  public void setTel(String tel) {
    this.tel = tel;
  }


  public String getEmail() {
    return email;
  }

  public void setEmail(String email) {
    this.email = email;
  }


  public long getScholarship() {
    return scholarship;
  }

  public void setScholarship(long scholarship) {
    this.scholarship = scholarship;
  }


  public String getTutorRequire() {
    return tutorRequire;
  }

  public void setTutorRequire(String tutorRequire) {
    this.tutorRequire = tutorRequire;
  }


  public long getDisplay() {
    return display;
  }

  public void setDisplay(long display) {
    this.display = display;
  }


  public long getEnrollmentNumber() {
    return enrollmentNumber;
  }

  public void setEnrollmentNumber(long enrollmentNumber) {
    this.enrollmentNumber = enrollmentNumber;
  }

}
